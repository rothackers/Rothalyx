std::string credential_fingerprint(std::string_view secret) {
    if (secret.empty()) {
        return {};
    }

    std::uint64_t hash = 1469598103934665603ULL;
    for (const unsigned char character : secret) {
        hash ^= static_cast<std::uint64_t>(character);
        hash *= 1099511628211ULL;
    }
    std::ostringstream stream;
    stream << "fp-" << std::hex << std::nouppercase << hash;
    return stream.str();
}

std::vector<FunctionInsight> merge_model_insights(
    std::vector<FunctionInsight> insights,
    const std::vector<ModelFunctionInsight>& model_insights
) {
    std::unordered_map<std::uint64_t, ModelFunctionInsight> by_address;
    for (const auto& insight : model_insights) {
        by_address[insight.entry_address] = insight;
    }

    for (auto& insight : insights) {
        const auto it = by_address.find(insight.entry_address);
        if (it == by_address.end()) {
            continue;
        }

        const auto& model = it->second;
        if (!model.suggested_name.empty()) {
            insight.suggested_name = model.suggested_name;
        }
        if (!model.summary.empty()) {
            insight.summary = model.summary;
        }
        insight.hints = model.hints;
        insight.patterns = model.patterns;
        insight.vulnerability_hints = model.vulnerability_hints;
    }

    return insights;
}

