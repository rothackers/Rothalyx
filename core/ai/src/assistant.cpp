std::vector<FunctionInsight> Assistant::analyze_program(
    const analysis::ProgramAnalysis& program,
    const std::optional<std::uint64_t> entry_point,
    const AssistantOptions& options,
    AssistantRunMetadata* out_metadata,
    ModelTransport* transport
) {
    if (out_metadata != nullptr) {
        *out_metadata = {};
    }

    const auto contexts = build_function_contexts(program, entry_point);
    auto insights = build_heuristic_insights(program, contexts);

    const auto openai = resolve_openai_options(options);
    const auto anthropic = resolve_anthropic_options(options);
    const auto gemini = resolve_gemini_options(options);
    const auto compatible = resolve_compatible_options(options);
    if (!openai.has_value() && !anthropic.has_value() && !gemini.has_value() && !compatible.has_value()) {
        if (out_metadata != nullptr) {
            out_metadata->backend = "heuristic";
            if (options.backend != AssistantBackend::Heuristic && options.backend != AssistantBackend::Auto) {
                out_metadata->warnings.push_back("Configured AI provider is missing required credentials or model settings.");
            }
        }
        return insights;
    }

    std::size_t max_functions = 12;
    if (openai.has_value()) {
        max_functions = openai->max_functions;
    } else if (anthropic.has_value()) {
        max_functions = anthropic->max_functions;
    } else if (gemini.has_value()) {
        max_functions = gemini->max_functions;
    } else if (compatible.has_value()) {
        max_functions = compatible->max_functions;
    }

    auto candidates = select_model_candidates(contexts, std::max<std::size_t>(1, max_functions));
    if (candidates.empty()) {
        if (out_metadata != nullptr) {
            out_metadata->backend = "heuristic";
            out_metadata->warnings.push_back("No functions were available for model-backed analysis.");
        }
        return insights;
    }

    std::unique_ptr<ModelTransport> owned_transport;
    if (transport == nullptr) {
#if defined(ZARA_HAS_CURL)
        owned_transport = std::make_unique<CurlModelTransport>();
        transport = owned_transport.get();
#else
        if (out_metadata != nullptr) {
            out_metadata->backend = "heuristic";
            out_metadata->warnings.push_back("Model-backed AI was requested, but libcurl support is not available in this build.");
        }
        return insights;
#endif
    }

    std::string response_json;
    std::string error;
    std::string request_json;
    std::optional<std::string> output_text;
    if (openai.has_value()) {
        request_json = build_openai_request_json(candidates, *openai);
    } else if (anthropic.has_value()) {
        request_json = build_anthropic_request_json(candidates, *anthropic);
    } else if (gemini.has_value()) {
        request_json = build_gemini_request_json(candidates, *gemini);
    } else if (compatible.has_value()) {
        request_json = build_compatible_request_json(candidates, *compatible);
    }
    if (!transport->create_response(request_json, options, response_json, error)) {
        if (out_metadata != nullptr) {
            out_metadata->backend = "heuristic";
            out_metadata->warnings.push_back(error.empty() ? "The model-backed AI request failed." : error);
        }
        return insights;
    }

    if (openai.has_value()) {
        output_text = parse_openai_output_text(response_json);
    } else if (anthropic.has_value()) {
        output_text = parse_anthropic_output_text(response_json);
    } else if (gemini.has_value()) {
        output_text = parse_gemini_output_text(response_json);
    } else if (compatible.has_value()) {
        output_text = parse_compatible_output_text(response_json);
    }
    if (!output_text.has_value()) {
        if (out_metadata != nullptr) {
            out_metadata->backend = "heuristic";
            out_metadata->warnings.push_back("The model response did not contain parseable JSON output.");
        }
        return insights;
    }

    const auto parsed = parse_model_function_insights(*output_text);
    if (parsed.empty()) {
        if (out_metadata != nullptr) {
            out_metadata->backend = "heuristic";
            out_metadata->warnings.push_back("The model response could not be parsed into Zara AI insight records.");
        }
        return insights;
    }

    if (out_metadata != nullptr) {
        if (openai.has_value()) {
            out_metadata->backend = "openai";
            out_metadata->model = openai->model;
            out_metadata->credential_fingerprint = credential_fingerprint(openai->api_key);
        } else if (anthropic.has_value()) {
            out_metadata->backend = "anthropic";
            out_metadata->model = anthropic->model;
            out_metadata->credential_fingerprint = credential_fingerprint(anthropic->api_key);
        } else if (gemini.has_value()) {
            out_metadata->backend = "gemini";
            out_metadata->model = gemini->model;
            out_metadata->credential_fingerprint = credential_fingerprint(gemini->api_key);
        } else if (compatible.has_value()) {
            out_metadata->backend = options.backend == AssistantBackend::LocalLLM ? "local_llm" : "openai_compatible";
            out_metadata->model = compatible->model;
            out_metadata->credential_fingerprint = credential_fingerprint(compatible->api_key);
        }
        out_metadata->used_remote_model = options.backend != AssistantBackend::LocalLLM;
    }
    return merge_model_insights(std::move(insights), parsed);
}

AssistantOptions Assistant::options_from_environment() {
    AssistantOptions options;
    if (const auto backend = environment_string("ZARA_AI_BACKEND"); backend.has_value()) {
        if (*backend == "openai") {
            options.backend = AssistantBackend::OpenAI;
        } else if (*backend == "anthropic") {
            options.backend = AssistantBackend::Anthropic;
        } else if (*backend == "gemini") {
            options.backend = AssistantBackend::Gemini;
        } else if (*backend == "openai_compatible") {
            options.backend = AssistantBackend::OpenAICompatible;
        } else if (*backend == "local_llm") {
            options.backend = AssistantBackend::LocalLLM;
        } else if (*backend == "auto") {
            options.backend = AssistantBackend::Auto;
        } else {
            options.backend = AssistantBackend::Heuristic;
        }
    } else if (environment_string("ZARA_OPENAI_API_KEY").has_value()) {
        options.backend = AssistantBackend::Auto;
    } else if (environment_string("ZARA_ANTHROPIC_API_KEY").has_value()) {
        options.backend = AssistantBackend::Anthropic;
    } else if (environment_string("ZARA_GEMINI_API_KEY").has_value()) {
        options.backend = AssistantBackend::Gemini;
    } else if (environment_string("ZARA_COMPATIBLE_MODEL").has_value() || environment_string("ZARA_COMPATIBLE_BASE_URL").has_value()) {
        options.backend = AssistantBackend::OpenAICompatible;
    } else if (environment_string("ZARA_LOCAL_LLM_MODEL").has_value() || environment_string("ZARA_LOCAL_LLM_BASE_URL").has_value()) {
        options.backend = AssistantBackend::LocalLLM;
    }

    if (const auto resolved = resolve_openai_options(options); resolved.has_value()) {
        options.openai = *resolved;
        options.openai->api_key.clear();
    }
    if (const auto resolved = resolve_anthropic_options(options); resolved.has_value()) {
        options.anthropic = *resolved;
        options.anthropic->api_key.clear();
    }
    if (const auto resolved = resolve_gemini_options(options); resolved.has_value()) {
        options.gemini = *resolved;
        options.gemini->api_key.clear();
    }
    if (const auto resolved = resolve_compatible_options(options); resolved.has_value()) {
        options.compatible = *resolved;
        options.compatible->api_key.clear();
    }
    return options;
}

std::string_view to_string(const AssistantBackend backend) noexcept {
    switch (backend) {
    case AssistantBackend::Heuristic:
        return "heuristic";
    case AssistantBackend::OpenAI:
        return "openai";
    case AssistantBackend::Anthropic:
        return "anthropic";
    case AssistantBackend::Gemini:
        return "gemini";
    case AssistantBackend::OpenAICompatible:
        return "openai_compatible";
    case AssistantBackend::LocalLLM:
        return "local_llm";
    case AssistantBackend::Auto:
    default:
        return "auto";
    }
}

