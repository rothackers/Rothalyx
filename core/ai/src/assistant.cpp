std::string build_model_prompt(const std::vector<FunctionContext>& contexts) {
    std::ostringstream payload;
    payload << "You are assisting a reverse engineering platform.\n"
            << "Analyze recovered binary functions and return concise analyst-grade rename, summary, pattern, and vulnerability hints.\n"
            << "Use the current function name when you are not confident in a better rename.\n"
            << "Keep summaries short and factual.\n"
            << "Do not invent imports, strings, or vulnerabilities that are not supported by the context.\n\n"
            << build_model_output_contract() << '\n';

    for (std::size_t index = 0; index < contexts.size(); ++index) {
        if (index > 0) {
            payload << "\n---\n";
        }
        payload << build_function_model_context(contexts[index]);
    }

    return payload.str();
}

