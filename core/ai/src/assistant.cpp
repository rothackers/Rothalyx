std::string normalize_import_name(std::string value) {
    const std::size_t bang = value.rfind('!');
    if (bang != std::string::npos) {
        value = value.substr(bang + 1);
    }
    return value;
}

bool is_autogenerated_name(const std::string_view name) {
    return name.rfind("sub_", 0) == 0;
}

bool contains_value(const std::vector<std::string>& values, const std::string_view needle) {
    return std::find(values.begin(), values.end(), needle) != values.end();
}

bool contains_any_import(const std::vector<std::string>& imports, const std::initializer_list<std::string_view> needles) {
    return std::any_of(
        needles.begin(),
        needles.end(),
        [&](const std::string_view needle) { return contains_value(imports, needle); }
    );
}

bool contains_token(const std::string& value, const std::string_view needle) {
    std::string lowered_value = value;
    std::transform(
        lowered_value.begin(),
        lowered_value.end(),
        lowered_value.begin(),
        [](const unsigned char character) { return static_cast<char>(std::tolower(character)); }
    );
    std::string lowered_needle(needle);
    std::transform(
        lowered_needle.begin(),
        lowered_needle.end(),
        lowered_needle.begin(),
        [](const unsigned char character) { return static_cast<char>(std::tolower(character)); }
    );
    return lowered_value.find(lowered_needle) != std::string::npos;
}

bool strings_contain_token(
    const std::vector<std::string>& strings,
    const std::initializer_list<std::string_view> needles
) {
    return std::any_of(
        strings.begin(),
        strings.end(),
        [&](const std::string& value) {
            return std::any_of(
                needles.begin(),
                needles.end(),
                [&](const std::string_view needle) { return contains_token(value, needle); }
            );
        }
    );
}

std::string sanitize_name(std::string value) {
    for (char& character : value) {
        if (!std::isalnum(static_cast<unsigned char>(character))) {
            character = '_';
        }
    }

    while (!value.empty() && value.back() == '_') {
        value.pop_back();
    }
    if (!value.empty() && std::isdigit(static_cast<unsigned char>(value.front()))) {
        value.insert(value.begin(), '_');
    }
    return value;
}

bool function_contains_address(const analysis::DiscoveredFunction& function, const std::uint64_t address) {
    for (const auto& block : function.graph.blocks()) {
        for (const auto& instruction : block.instructions) {
            if (instruction.address == address) {
                return true;
            }
        }
    }
    return false;
}

std::string choose_summary(
    const analysis::DiscoveredFunction& function,
    const std::vector<std::string>& imports,
    const std::vector<std::string>& strings,
    const bool is_entry
) {
    if (is_entry || std::find(imports.begin(), imports.end(), "__libc_start_main") != imports.end()) {
        return "Runtime entry/bootstrap routine.";
    }
    if (std::find(imports.begin(), imports.end(), "nanosleep") != imports.end()) {
        return "Timing or sleep-related helper.";
    }
    if (std::find(imports.begin(), imports.end(), "strlen") != imports.end() &&
        std::find(imports.begin(), imports.end(), "memcmp") != imports.end()) {
        return "String and memory comparison routine.";
    }
    if (std::find(imports.begin(), imports.end(), "memcmp") != imports.end()) {
        return "Memory comparison routine.";
    }
    if (std::find(imports.begin(), imports.end(), "strlen") != imports.end()) {
        return "String-processing helper.";
    }
    if (!strings.empty()) {
        return "Routine references embedded strings and likely drives user-visible behavior.";
    }

    std::size_t instruction_count = 0;
    for (const auto& block : function.graph.blocks()) {
        instruction_count += block.instructions.size();
    }
    std::ostringstream stream;
    stream << "General routine with " << function.graph.blocks().size() << " blocks and " << instruction_count
           << " instructions.";
    return stream.str();
}

std::string choose_suggested_name(
    const analysis::DiscoveredFunction& function,
    const std::vector<std::string>& imports,
    const std::vector<std::string>& strings,
    const bool is_entry
) {
    if (!is_autogenerated_name(function.name)) {
        return function.name;
    }
    if (is_entry || std::find(imports.begin(), imports.end(), "__libc_start_main") != imports.end()) {
        return "entry_startup";
    }
    if (std::find(imports.begin(), imports.end(), "nanosleep") != imports.end()) {
        return "sleep_helper";
    }
    if (std::find(imports.begin(), imports.end(), "strlen") != imports.end() &&
        std::find(imports.begin(), imports.end(), "memcmp") != imports.end()) {
        return "string_compare_helper";
    }
    if (!strings.empty()) {
        const std::string base = sanitize_name(strings.front().substr(0, std::min<std::size_t>(24, strings.front().size())));
        if (!base.empty()) {
            return base;
        }
    }
    return function.name;
}

std::vector<std::string> collect_hints(
    const analysis::DiscoveredFunction& function,
    const std::vector<std::string>& imports,
    const std::vector<std::string>& strings,
    const bool is_entry
) {
    std::vector<std::string> hints;
    if (is_entry) {
        hints.push_back("Entry-point context suggests process/bootstrap setup.");
    }
    if (std::find(imports.begin(), imports.end(), "__libc_start_main") != imports.end()) {
        hints.push_back("Calls __libc_start_main, which strongly indicates startup code.");
    }
    if (std::find(imports.begin(), imports.end(), "nanosleep") != imports.end()) {
        hints.push_back("Calls nanosleep, which suggests throttling, retry delay, or loop pacing.");
    }
    if (std::find(imports.begin(), imports.end(), "memcmp") != imports.end()) {
        hints.push_back("Calls memcmp, which suggests byte-sequence comparison logic.");
    }
    if (std::find(imports.begin(), imports.end(), "strlen") != imports.end()) {
        hints.push_back("Calls strlen, which suggests string-oriented processing.");
    }
    if (std::find(imports.begin(), imports.end(), "__stack_chk_fail") != imports.end()) {
        hints.push_back("Contains stack-canary failure path.");
    }
    for (std::size_t index = 0; index < std::min<std::size_t>(strings.size(), 2); ++index) {
        hints.push_back("References string: " + strings[index]);
    }
    if (function.graph.blocks().size() >= 10) {
        hints.push_back("High block count indicates non-trivial control flow.");
    }
    return hints;
}

std::vector<PatternDetection> collect_patterns(
    const analysis::DiscoveredFunction& function,
    const std::vector<std::string>& imports,
    const std::vector<std::string>& strings,
    const bool is_entry
) {
    std::vector<PatternDetection> patterns;
    std::set<std::pair<std::string, std::string>> seen;

    auto add_pattern = [&](std::string category, std::string label, std::string confidence, std::string detail) {
        const auto key = std::make_pair(category, label);
        if (!seen.insert(key).second) {
            return;
        }
        patterns.push_back(
            PatternDetection{
                .category = std::move(category),
                .label = std::move(label),
                .confidence = std::move(confidence),
                .detail = std::move(detail),
            }
        );
    };

    if (is_entry || contains_value(imports, "__libc_start_main")) {
        add_pattern(
            "bootstrap",
            "startup/bootstrap",
            "high",
            "Entry-point context and startup imports indicate process bootstrap logic."
        );
    }
    if (contains_any_import(imports, {"strlen", "strcmp", "strncmp", "memcmp", "strcasecmp"}) ||
        strings_contain_token(strings, {"password", "token", "auth", "credential"})) {
        add_pattern(
            "comparison",
            "authentication or comparison routine",
            "medium",
            "Comparison-style imports or credential-like strings suggest validation logic."
        );
    }
    if (contains_any_import(imports, {"open", "fopen", "read", "write", "close", "stat"})) {
        add_pattern("io", "file I/O", "medium", "File-oriented imports suggest parsing, logging, or file processing behavior.");
    }
    if (contains_any_import(imports, {"socket", "connect", "accept", "send", "recv"}) ||
        strings_contain_token(strings, {"http", "https", "user-agent", "host:", "cookie"})) {
        add_pattern("network", "network I/O", "medium", "Socket imports or protocol strings suggest network-facing behavior.");
    }
    if (contains_any_import(imports, {"system", "popen", "execve", "CreateProcessA", "CreateProcessW"}) ||
        strings_contain_token(strings, {"/bin/sh", "cmd.exe", "powershell"})) {
        add_pattern("execution", "command execution", "high", "Command-launch imports or shell strings indicate process-spawn behavior.");
    }
    if (contains_any_import(imports, {"AES_encrypt", "AES_decrypt", "MD5", "SHA1", "SHA256", "crypt"}) ||
        strings_contain_token(strings, {"aes", "sha", "md5", "crc", "encrypt", "decrypt"})) {
        add_pattern("crypto", "cryptography or checksum", "medium", "Crypto-like imports or strings suggest hashing, checksums, or encryption code.");
    }
    if (!function.graph.loops().empty()) {
        add_pattern(
            "control_flow",
            "loop-heavy processing",
            function.graph.loops().size() > 1 ? "high" : "medium",
            "Recovered loop headers indicate iterative processing."
        );
    }
    if (!function.graph.switches().empty()) {
        add_pattern(
            "control_flow",
            "dispatcher or switch table",
            "high",
            "Recovered switch or jump-table targets indicate a dispatcher-style routine."
        );
    }
    if (function.summary.stack_frame_size >= 0x80) {
        add_pattern(
            "stack",
            "stack-local buffer handling",
            "medium",
            "Recovered stack frame size suggests non-trivial local stack storage."
        );
    }

    return patterns;
}

std::vector<VulnerabilityHint> collect_vulnerability_hints(
    const analysis::DiscoveredFunction& function,
    const std::vector<std::string>& imports,
    const std::vector<std::string>& strings
) {
    std::vector<VulnerabilityHint> hints;
    std::set<std::string> seen;

    auto add_hint = [&](std::string severity, std::string title, std::string detail) {
        const std::string key = severity + "|" + title + "|" + detail;
        if (!seen.insert(key).second) {
            return;
        }
        hints.push_back(
            VulnerabilityHint{
                .severity = std::move(severity),
                .title = std::move(title),
                .detail = std::move(detail),
            }
        );
    };

    if (contains_any_import(imports, {"gets", "strcpy", "strcat", "sprintf", "vsprintf", "scanf", "sscanf", "fscanf"})) {
        add_hint(
            "high",
            "Unsafe stack or string copy surface",
            "Unsafe C library entry points suggest overflow-prone input or string handling."
        );
    }
    if (contains_any_import(imports, {"memcpy", "memmove", "read", "recv"}) && function.summary.stack_frame_size >= 0x40) {
        add_hint(
            "medium",
            "Input reaches sizeable stack frame",
            "Input or copy primitives execute in a function with a recovered local stack frame, which is worth stack-bound review."
        );
    }
    if (contains_any_import(imports, {"printf", "fprintf", "snprintf", "vprintf"}) &&
        strings_contain_token(strings, {"%", "%s", "%x", "%n"})) {
        add_hint(
            "medium",
            "Format-string exposure",
            "Formatting imports plus literal format strings indicate a potential format-string surface."
        );
    }
    if (contains_any_import(imports, {"system", "popen", "execve"}) ||
        strings_contain_token(strings, {"/bin/sh", "cmd.exe", "sh -c"})) {
        add_hint(
            "high",
            "Command execution path",
            "Shell strings or command-execution imports indicate a direct command-launch primitive."
        );
    }
    if (!function.graph.switches().empty() && !function.summary.indirect_resolutions.empty()) {
        add_hint(
            "low",
            "Indirect control flow",
            "Indirect dispatch recovered from the CFG can hide sink selection and deserves control-flow review."
        );
    }

    return hints;
}

FunctionInsight build_heuristic_insight(
    const analysis::DiscoveredFunction& function,
    const std::vector<std::string>& imports,
    const std::vector<std::string>& strings,
    const bool is_entry
) {
    return FunctionInsight{
        .entry_address = function.entry_address,
        .current_name = function.name,
        .suggested_name = choose_suggested_name(function, imports, strings, is_entry),
        .summary = choose_summary(function, imports, strings, is_entry),
        .hints = collect_hints(function, imports, strings, is_entry),
        .patterns = collect_patterns(function, imports, strings, is_entry),
        .vulnerability_hints = collect_vulnerability_hints(function, imports, strings),
    };
}

std::unordered_map<std::uint64_t, std::vector<std::string>> collect_imports_by_function(
    const analysis::ProgramAnalysis& program
) {
    std::unordered_map<std::uint64_t, std::vector<std::string>> imports_by_function;
    for (const auto& edge : program.call_graph) {
        if (!edge.is_import) {
            continue;
        }
        imports_by_function[edge.caller_entry].push_back(normalize_import_name(edge.callee_name));
    }

    for (auto& [_, imports] : imports_by_function) {
        std::sort(imports.begin(), imports.end());
        imports.erase(std::unique(imports.begin(), imports.end()), imports.end());
    }

    return imports_by_function;
}

std::unordered_map<std::uint64_t, std::vector<std::string>> collect_strings_by_function(
    const analysis::ProgramAnalysis& program
) {
    std::unordered_map<std::uint64_t, std::vector<std::string>> strings_by_function;
    for (const auto& function : program.functions) {
        auto& strings = strings_by_function[function.entry_address];
        for (const auto& xref : program.xrefs) {
            if (xref.kind != xrefs::CrossReferenceKind::String) {
                continue;
            }
            if (function_contains_address(function, xref.from_address) && !xref.label.empty()) {
                strings.push_back(xref.label);
            }
        }
        std::sort(strings.begin(), strings.end());
        strings.erase(std::unique(strings.begin(), strings.end()), strings.end());
    }
    return strings_by_function;
}

std::vector<FunctionContext> build_function_contexts(
    const analysis::ProgramAnalysis& program,
    const std::optional<std::uint64_t> entry_point
) {
    const auto imports_by_function = collect_imports_by_function(program);
    const auto strings_by_function = collect_strings_by_function(program);

    std::vector<FunctionContext> contexts;
    contexts.reserve(program.functions.size());

    for (const auto& function : program.functions) {
        FunctionContext context;
        context.function = &function;
        if (const auto imports_it = imports_by_function.find(function.entry_address); imports_it != imports_by_function.end()) {
            context.imports = imports_it->second;
        }
        if (const auto strings_it = strings_by_function.find(function.entry_address); strings_it != strings_by_function.end()) {
            context.strings = strings_it->second;
        }
        context.is_entry = entry_point.has_value() && *entry_point == function.entry_address;

        int score = 0;
        if (context.is_entry) {
            score += 500;
        }
        score += static_cast<int>(context.imports.size()) * 30;
        score += static_cast<int>(context.strings.size()) * 25;
        score += static_cast<int>(function.graph.blocks().size()) * 4;
        score += static_cast<int>(function.summary.arguments.size()) * 10;
        score += static_cast<int>(function.summary.locals.size()) * 6;
        score += static_cast<int>(function.graph.loops().size()) * 12;
        score += static_cast<int>(function.graph.switches().size()) * 20;
        if (!function.summary.indirect_resolutions.empty()) {
            score += 25;
        }
        if (function.summary.stack_frame_size >= 0x40) {
            score += 15;
        }
        context.score = score;
        contexts.push_back(std::move(context));
    }

    return contexts;
}

std::vector<FunctionInsight> build_heuristic_insights(
    const analysis::ProgramAnalysis&,
    const std::vector<FunctionContext>& contexts
) {
    std::vector<FunctionInsight> insights;
    insights.reserve(contexts.size());
    for (const auto& context : contexts) {
        insights.push_back(
            build_heuristic_insight(*context.function, context.imports, context.strings, context.is_entry)
        );
    }
    std::sort(
        insights.begin(),
        insights.end(),
        [](const FunctionInsight& lhs, const FunctionInsight& rhs) { return lhs.entry_address < rhs.entry_address; }
    );
    return insights;
}

std::string first_lines(const std::string& value, const std::size_t max_lines, const std::size_t max_chars) {
    std::istringstream stream(value);
    std::ostringstream output;
    std::string line;
    std::size_t emitted = 0;
    std::size_t chars = 0;
    while (std::getline(stream, line) && emitted < max_lines && chars < max_chars) {
        if (chars + line.size() > max_chars) {
            line = line.substr(0, max_chars - chars);
        }
        if (emitted > 0) {
            output << '\n';
            ++chars;
        }
        output << line;
        chars += line.size();
        ++emitted;
    }
    return output.str();
}

std::string json_escape(const std::string_view value) {
    std::string escaped;
    escaped.reserve(value.size() + 8);
    for (const char character : value) {
        switch (character) {
        case '\\':
            escaped += "\\\\";
            break;
        case '"':
            escaped += "\\\"";
            break;
        case '\n':
            escaped += "\\n";
            break;
        case '\r':
            escaped += "\\r";
            break;
        case '\t':
            escaped += "\\t";
            break;
        default:
            escaped.push_back(character);
            break;
        }
    }
    return escaped;
}

std::string json_string(const std::string_view value) {
    return "\"" + json_escape(value) + "\"";
}

std::string format_address(const std::uint64_t address) {
    std::ostringstream stream;
    stream << "0x" << std::uppercase << std::hex << address;
    return stream.str();
}

std::string join(const std::vector<std::string>& values) {
    std::ostringstream stream;
    for (std::size_t index = 0; index < values.size(); ++index) {
        if (index > 0) {
            stream << ", ";
        }
        stream << values[index];
    }
    return stream.str();
}

std::string build_function_model_context(const FunctionContext& context) {
    const auto& function = *context.function;
    std::ostringstream stream;
    stream << "entry=" << format_address(function.entry_address) << '\n';
    stream << "name=" << function.name << '\n';
    stream << "section=" << function.section_name << '\n';
    stream << "blocks=" << function.graph.blocks().size() << '\n';

    std::size_t instruction_count = 0;
    for (const auto& block : function.graph.blocks()) {
        instruction_count += block.instructions.size();
    }
    stream << "instructions=" << instruction_count << '\n';
    stream << "calling_convention=" << analysis::to_string(function.summary.calling_convention) << '\n';
    stream << "stack_frame_size=" << function.summary.stack_frame_size << '\n';
    stream << "loops=" << function.graph.loops().size() << '\n';
    stream << "switches=" << function.graph.switches().size() << '\n';
    stream << "imports=" << (context.imports.empty() ? "-" : first_lines(join(context.imports), 1, 256)) << '\n';
    stream << "strings=" << (context.strings.empty() ? "-" : first_lines(join(context.strings), 3, 256)) << '\n';
    stream << "locals=" << function.summary.locals.size() << '\n';
    stream << "arguments=" << function.summary.arguments.size() << '\n';
    stream << "indirect_targets=" << function.summary.indirect_resolutions.size() << '\n';
    const std::string pseudocode_preview = first_lines(function.decompiled.pseudocode, 10, 900);
    if (!pseudocode_preview.empty()) {
        stream << "pseudocode:\n" << pseudocode_preview << '\n';
    }
    return stream.str();
}

std::vector<FunctionContext> select_model_candidates(
    std::vector<FunctionContext> contexts,
    const std::size_t max_functions
) {
    std::sort(
        contexts.begin(),
        contexts.end(),
        [](const FunctionContext& left, const FunctionContext& right) {
            return std::pair(-left.score, left.function->entry_address) < std::pair(-right.score, right.function->entry_address);
        }
    );
    if (contexts.size() > max_functions) {
        contexts.resize(max_functions);
    }
    std::sort(
        contexts.begin(),
        contexts.end(),
        [](const FunctionContext& left, const FunctionContext& right) {
            return left.function->entry_address < right.function->entry_address;
        }
    );
    return contexts;
}

