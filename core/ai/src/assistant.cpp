std::size_t skip_whitespace(const std::string_view text, std::size_t position) {
    while (position < text.size() && std::isspace(static_cast<unsigned char>(text[position])) != 0) {
        ++position;
    }
    return position;
}

std::optional<std::size_t> find_field_value_start(const std::string_view json, const std::string_view field) {
    const std::string key = "\"" + std::string(field) + "\"";
    const auto key_position = json.find(key);
    if (key_position == std::string_view::npos) {
        return std::nullopt;
    }
    const auto colon_position = json.find(':', key_position + key.size());
    if (colon_position == std::string_view::npos) {
        return std::nullopt;
    }
    return skip_whitespace(json, colon_position + 1);
}

std::optional<std::size_t> find_matching_delimiter(
    const std::string_view text,
    const std::size_t open_position,
    const char open_char,
    const char close_char
) {
    int depth = 0;
    bool in_string = false;
    bool escaped = false;
    for (std::size_t index = open_position; index < text.size(); ++index) {
        const char character = text[index];
        if (escaped) {
            escaped = false;
            continue;
        }
        if (character == '\\') {
            escaped = true;
            continue;
        }
        if (character == '"') {
            in_string = !in_string;
            continue;
        }
        if (in_string) {
            continue;
        }
        if (character == open_char) {
            ++depth;
        } else if (character == close_char) {
            --depth;
            if (depth == 0) {
                return index;
            }
        }
    }
    return std::nullopt;
}

std::string json_unescape(const std::string_view value) {
    std::string output;
    output.reserve(value.size());

    bool escaped = false;
    for (std::size_t index = 0; index < value.size(); ++index) {
        const char character = value[index];
        if (!escaped) {
            if (character == '\\') {
                escaped = true;
                continue;
            }
            output.push_back(character);
            continue;
        }

        escaped = false;
        switch (character) {
        case '\\':
            output.push_back('\\');
            break;
        case '"':
            output.push_back('"');
            break;
        case 'n':
            output.push_back('\n');
            break;
        case 'r':
            output.push_back('\r');
            break;
        case 't':
            output.push_back('\t');
            break;
        case 'u':
            if (index + 4 < value.size()) {
                index += 4;
            }
            break;
        default:
            output.push_back(character);
            break;
        }
    }
    return output;
}

std::optional<std::string> extract_json_string_value(const std::string_view json, const std::string_view field) {
    const auto value_position = find_field_value_start(json, field);
    if (!value_position.has_value() || *value_position >= json.size() || json[*value_position] != '"') {
        return std::nullopt;
    }

    bool escaped = false;
    for (std::size_t index = *value_position + 1; index < json.size(); ++index) {
        const char character = json[index];
        if (escaped) {
            escaped = false;
            continue;
        }
        if (character == '\\') {
            escaped = true;
            continue;
        }
        if (character == '"') {
            return json_unescape(json.substr(*value_position + 1, index - *value_position - 1));
        }
    }

    return std::nullopt;
}

std::vector<std::string> extract_json_string_array(const std::string_view json, const std::string_view field) {
    const auto value_position = find_field_value_start(json, field);
    if (!value_position.has_value() || *value_position >= json.size() || json[*value_position] != '[') {
        return {};
    }

    const auto close_position = find_matching_delimiter(json, *value_position, '[', ']');
    if (!close_position.has_value()) {
        return {};
    }

    std::vector<std::string> values;
    std::size_t cursor = *value_position + 1;
    while (cursor < *close_position) {
        cursor = skip_whitespace(json, cursor);
        if (cursor >= *close_position) {
            break;
        }
        if (json[cursor] != '"') {
            ++cursor;
            continue;
        }

        bool escaped = false;
        std::size_t end = cursor + 1;
        for (; end < *close_position; ++end) {
            const char character = json[end];
            if (escaped) {
                escaped = false;
                continue;
            }
            if (character == '\\') {
                escaped = true;
                continue;
            }
            if (character == '"') {
                break;
            }
        }
        if (end >= *close_position) {
            break;
        }
        values.push_back(json_unescape(json.substr(cursor + 1, end - cursor - 1)));
        cursor = end + 1;
    }
    return values;
}

std::vector<std::string> extract_json_object_array(const std::string_view json, const std::string_view field) {
    const auto value_position = find_field_value_start(json, field);
    if (!value_position.has_value() || *value_position >= json.size() || json[*value_position] != '[') {
        return {};
    }

    const auto close_position = find_matching_delimiter(json, *value_position, '[', ']');
    if (!close_position.has_value()) {
        return {};
    }

    std::vector<std::string> values;
    std::size_t cursor = *value_position + 1;
    while (cursor < *close_position) {
        cursor = skip_whitespace(json, cursor);
        if (cursor >= *close_position) {
            break;
        }
        if (json[cursor] != '{') {
            ++cursor;
            continue;
        }

        const auto object_end = find_matching_delimiter(json, cursor, '{', '}');
        if (!object_end.has_value() || *object_end > *close_position) {
            break;
        }
        values.emplace_back(json.substr(cursor, *object_end - cursor + 1));
        cursor = *object_end + 1;
    }
    return values;
}

std::optional<std::uint64_t> parse_address_string(const std::string_view text) {
    if (text.empty()) {
        return std::nullopt;
    }

    try {
        std::size_t consumed = 0;
        const int base = text.starts_with("0x") || text.starts_with("0X") ? 16 : 10;
        const auto value = std::stoull(std::string(text), &consumed, base);
        if (consumed != text.size()) {
            return std::nullopt;
        }
        return static_cast<std::uint64_t>(value);
    } catch (...) {
        return std::nullopt;
    }
}

std::vector<ModelFunctionInsight> parse_model_function_insights(const std::string& output_text) {
    std::vector<ModelFunctionInsight> parsed;
    for (const auto& object : extract_json_object_array(output_text, "functions")) {
        const auto entry_address = extract_json_string_value(object, "entry_address");
        const auto parsed_entry = entry_address.has_value() ? parse_address_string(*entry_address) : std::nullopt;
        if (!parsed_entry.has_value()) {
            continue;
        }

        ModelFunctionInsight insight;
        insight.entry_address = *parsed_entry;
        insight.suggested_name = extract_json_string_value(object, "suggested_name").value_or("");
        insight.summary = extract_json_string_value(object, "summary").value_or("");
        insight.hints = extract_json_string_array(object, "hints");

        for (const auto& pattern_object : extract_json_object_array(object, "patterns")) {
            insight.patterns.push_back(
                PatternDetection{
                    .category = extract_json_string_value(pattern_object, "category").value_or(""),
                    .label = extract_json_string_value(pattern_object, "label").value_or(""),
                    .confidence = extract_json_string_value(pattern_object, "confidence").value_or(""),
                    .detail = extract_json_string_value(pattern_object, "detail").value_or(""),
                }
            );
        }

        for (const auto& hint_object : extract_json_object_array(object, "vulnerability_hints")) {
            insight.vulnerability_hints.push_back(
                VulnerabilityHint{
                    .severity = extract_json_string_value(hint_object, "severity").value_or(""),
                    .title = extract_json_string_value(hint_object, "title").value_or(""),
                    .detail = extract_json_string_value(hint_object, "detail").value_or(""),
                }
            );
        }

        parsed.push_back(std::move(insight));
    }

    return parsed;
}

std::optional<std::string> parse_openai_output_text(const std::string& response_json) {
    const auto output_text = extract_json_string_value(response_json, "output_text");
    if (!output_text.has_value()) {
        return std::nullopt;
    }

    const auto first_non_space = output_text->find_first_not_of(" \t\r\n");
    const auto last_non_space = output_text->find_last_not_of(" \t\r\n");
    if (first_non_space == std::string::npos || last_non_space == std::string::npos) {
        return std::nullopt;
    }
    if ((*output_text)[first_non_space] != '{' || (*output_text)[last_non_space] != '}') {
        return std::nullopt;
    }
    return output_text;
}

std::optional<std::string> normalize_json_output_text(std::string text) {
    const auto first_non_space = text.find_first_not_of(" \t\r\n");
    if (first_non_space == std::string::npos) {
        return std::nullopt;
    }
    text.erase(0, first_non_space);

    if (text.rfind("```", 0) == 0) {
        const auto first_newline = text.find('\n');
        if (first_newline == std::string::npos) {
            return std::nullopt;
        }
        text.erase(0, first_newline + 1);
        const auto fence_end = text.rfind("```");
        if (fence_end != std::string::npos) {
            text.erase(fence_end);
        }
    }

    const auto object_start = text.find('{');
    if (object_start == std::string::npos) {
        return std::nullopt;
    }
    const auto object_end = find_matching_delimiter(text, object_start, '{', '}');
    if (!object_end.has_value()) {
        return std::nullopt;
    }
    return text.substr(object_start, *object_end - object_start + 1);
}

